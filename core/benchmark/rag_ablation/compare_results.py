"""Create the single LaTeX table for the paired RAG ablation."""

from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd


HERE = Path(__file__).resolve().parent
DEFAULT_RESULTS = HERE / "results.csv"
DEFAULT_OUTPUT = HERE / "rag_ablation_table.tex"
EXPECTED_CONDITIONS = ("RAG_OFF", "RAG_ON")
EXPECTED_TASKS = 20


def _as_bool(series: pd.Series) -> pd.Series:
    return series.astype(str).str.strip().str.lower().isin({"true", "1", "yes"})


def _pct(value: float) -> str:
    return f"{value:.2f}\\%"


def generate_comparison(
    results_path: Path = DEFAULT_RESULTS,
    output_path: Path = DEFAULT_OUTPUT,
) -> pd.DataFrame:
    frame = pd.read_csv(results_path)
    required = {
        "Condition",
        "TaskID",
        "ProjectHash",
        "FirstAttemptCompileCollectionPassed",
        "FirstAttemptContextError",
        "EventualAccepted",
        "ScoreComplete",
        "FinalScore",
    }
    missing = required - set(frame.columns)
    if missing:
        raise ValueError(
            "RAG ablation results are missing columns: "
            + ", ".join(sorted(missing))
        )
    duplicates = frame.duplicated(["Condition", "TaskID"], keep=False)
    if duplicates.any():
        raise ValueError("Duplicate Condition/TaskID rows would bias the ablation.")

    counts = frame.groupby("Condition")["TaskID"].nunique().to_dict()
    if any(counts.get(condition) != EXPECTED_TASKS for condition in EXPECTED_CONDITIONS):
        raise ValueError(
            "A final RAG table requires 20 paired tasks per condition; "
            f"found {counts}."
        )
    paired = frame.pivot(index="TaskID", columns="Condition", values="ProjectHash")
    if paired[list(EXPECTED_CONDITIONS)].isna().any().any():
        raise ValueError("RAG conditions do not contain the same TaskID set.")
    if (paired["RAG_OFF"] != paired["RAG_ON"]).any():
        raise ValueError("RAG conditions were evaluated on different project hashes.")

    for column in (
        "FirstAttemptCompileCollectionPassed",
        "FirstAttemptContextError",
        "EventualAccepted",
        "ScoreComplete",
    ):
        frame[column] = _as_bool(frame[column])
    frame["FinalScore"] = pd.to_numeric(frame["FinalScore"], errors="coerce")

    rows: list[dict[str, object]] = []
    for condition in EXPECTED_CONDITIONS:
        group = frame[frame["Condition"] == condition]
        complete_scores = group.loc[group["ScoreComplete"], "FinalScore"].dropna()
        rows.append(
            {
                "Condition": condition,
                "Tasks": len(group),
                "CompileCollectionFirst_pct": 100.0
                * group["FirstAttemptCompileCollectionPassed"].mean(),
                "ContextErrorFirst_pct": 100.0
                * group["FirstAttemptContextError"].mean(),
                "EventualAccepted_pct": 100.0 * group["EventualAccepted"].mean(),
                "FinalScore_mean_pct": complete_scores.mean(),
                "FinalScore_n": len(complete_scores),
            }
        )
    summary = pd.DataFrame(rows)
    summary.to_csv(output_path.with_suffix(".csv"), index=False)

    labels = {
        "RAG_OFF": "Không RAG",
        "RAG_ON": "Có RAG",
    }
    lines = [
        "% Auto-generated by core/benchmark/rag_ablation/compare_results.py",
        "\\begin{table}[t]",
        "  \\centering",
        "  \\caption{Ảnh hưởng của RAG trên 20 project-task ghép cặp của Qwen2.5-Coder 7B.}",
        "  \\label{tab:rag-ablation}",
        "  \\begin{tabular}{lrrrr}",
        "    \\hline",
        "    Cấu hình & Compile+collect & Lỗi ngữ cảnh & Eventual accepted & Final score \\\\",
        "    \\hline",
    ]
    for row in summary.itertuples(index=False):
        lines.append(
            "    "
            + " & ".join(
                (
                    labels[str(row.Condition)],
                    _pct(float(row.CompileCollectionFirst_pct)),
                    _pct(float(row.ContextErrorFirst_pct)),
                    _pct(float(row.EventualAccepted_pct)),
                    _pct(float(row.FinalScore_mean_pct)),
                )
            )
            + " \\\\"
        )
    lines.extend(
        [
            "    \\hline",
            "  \\end{tabular}",
            "  \\begin{flushleft}",
            "  \\footnotesize Compile+collect và lỗi ngữ cảnh được đo ở attempt đầu. "
            "Final score chỉ dùng evaluation hoàn tất; số mẫu được lưu trong file CSV đi kèm.",
            "  \\end{flushleft}",
            "\\end{table}",
            "",
        ]
    )
    output_path.write_text("\n".join(lines), encoding="utf-8")
    return summary


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--input", type=Path, default=DEFAULT_RESULTS)
    parser.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    args = parser.parse_args()
    summary = generate_comparison(args.input, args.output)
    print(summary.round(2).to_string(index=False))
    print(f"LaTeX table: {args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
