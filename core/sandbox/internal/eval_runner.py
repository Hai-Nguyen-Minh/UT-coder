"""Isolated benchmark-quality evaluator for generated Python tests.

This evaluator is intentionally separate from the fast reflection sandbox.
The generator receives cheap pytest/coverage feedback; mutation testing runs
once, against the final candidate, after all deterministic hard gates pass.
"""

