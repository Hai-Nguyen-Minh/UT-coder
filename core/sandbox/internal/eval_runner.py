MODULE_NAME = "module_under_test"
TEST_FILE_NAME = f"test_{MODULE_NAME}.py"
REPORTER_FILE_NAME = "utcoder_eval_plugin.py"
_WORKSPACE_RESERVED_PATHS = {
    f"{MODULE_NAME}.py",
    TEST_FILE_NAME,
    REPORTER_FILE_NAME,
    "__init__.py",
}
_ANSI_ESCAPE = re.compile(r"\x1b(?:[@-Z\\-_]|\[[0-?]*[ -/]*[@-~])")
_MUTMUT_EMOJIS = {
    "killed": "🎉",
    "no_tests": "🫥",
    "timed_out": "⏰",
    "suspicious": "🤔",
    "survived": "🙁",
    "skipped": "🔇",
    "interrupted": "🛑",
    "segfault": "💥",
}

_PYTEST_REPORTER = r'''"""Trusted per-process pytest report plugin generated by UTcoder."""
import json
import os

_outcomes = {}
_collected = 0
_collection_errors = 0


def _record(nodeid, outcome):
    priority = {
        "skipped": 1,
        "xfailed": 2,
        "passed": 3,
        "xpassed": 4,
        "failed": 5,
        "errors": 6,
    }
    previous = _outcomes.get(nodeid)
    if previous is None or priority[outcome] >= priority[previous]:
        _outcomes[nodeid] = outcome


def pytest_collection_finish(session):
    global _collected
    _collected = len(session.items)


def pytest_collectreport(report):
    global _collection_errors
    if report.failed:
        _collection_errors += 1


def pytest_runtest_logreport(report):
    was_xfail = bool(getattr(report, "wasxfail", False))
    if report.when == "setup":
        if report.failed:
            _record(report.nodeid, "errors")
        elif report.skipped:
            _record(report.nodeid, "xfailed" if was_xfail else "skipped")
        return
    if report.when == "call":
        if report.passed:
            _record(report.nodeid, "xpassed" if was_xfail else "passed")
        elif report.failed:
            _record(report.nodeid, "xpassed" if was_xfail else "failed")
        elif report.skipped:
            _record(report.nodeid, "xfailed" if was_xfail else "skipped")
        return
    if report.when == "teardown" and report.failed:
        _record(report.nodeid, "errors")


def pytest_sessionfinish(session, exitstatus):
    summary = {
        "collected": _collected,
        "passed": 0,
        "failed": 0,
        "errors": _collection_errors,
        "skipped": 0,
        "xfailed": 0,
        "xpassed": 0,
    }
    for outcome in _outcomes.values():
        summary[outcome] += 1
    payload = {"exitcode": int(exitstatus), "summary": summary}
    destination = os.environ.get("UTCODER_PYTEST_REPORT", "pytest_report.json")
    with open(destination, "w", encoding="utf-8") as handle:
        json.dump(payload, handle, sort_keys=True)
'''


@dataclass(frozen=True)
class EvaluationConfig:
    compile_timeout_seconds: float = 10.0
    collection_timeout_seconds: float = 30.0
    baseline_timeout_seconds: float = 30.0
    coverage_timeout_seconds: float = 45.0
    mutation_timeout_seconds: float = 180.0
    mutation_max_children: int = 2
    memory_mb: int = 768
    mutation_memory_mb: int = 1536
    max_processes: int = 32

    @classmethod
    def from_environment(cls) -> "EvaluationConfig":
        """Allow Ubuntu operators to tune expensive limits without code edits."""

        def number(name: str, default: float, cast):
            raw = os.environ.get(name)
            if raw is None:
                return default
            try:
                value = cast(raw)
            except (TypeError, ValueError):
                return default
            return value if value > 0 else default

        return cls(
            compile_timeout_seconds=number("UTCODER_EVAL_COMPILE_TIMEOUT", 10.0, float),
            collection_timeout_seconds=number("UTCODER_EVAL_COLLECTION_TIMEOUT", 30.0, float),
            baseline_timeout_seconds=number("UTCODER_EVAL_BASELINE_TIMEOUT", 30.0, float),
            coverage_timeout_seconds=number("UTCODER_EVAL_COVERAGE_TIMEOUT", 45.0, float),
            mutation_timeout_seconds=number("UTCODER_EVAL_MUTATION_TIMEOUT", 180.0, float),
            mutation_max_children=number("UTCODER_EVAL_MUTATION_CHILDREN", 2, int),
            memory_mb=number("UTCODER_EVAL_MEMORY_MB", 768, int),
            mutation_memory_mb=number("UTCODER_EVAL_MUTATION_MEMORY_MB", 1536, int),
            max_processes=number("UTCODER_EVAL_MAX_PROCESSES", 32, int),
        )


@dataclass
class _ProcessOutcome:
    returncode: int | None
    stdout: str
    stderr: str
    duration_seconds: float
    timed_out: bool = False
    start_error: str = ""

    @property
    def output(self) -> str:
        return "\n".join(part for part in (self.stdout, self.stderr) if part).strip()


def _short_log(value: str, limit: int = 3000) -> str:
    value = str(value or "").strip()
    return value if len(value) <= limit else value[-limit:]


def _prepare_test_code(file_name: str, source_code: str, test_code: str) -> str:
    """Apply the same deterministic normalization as the reflection sandbox."""

    original_stem = Path(file_name).stem
    if original_stem != MODULE_NAME:
        test_code = re.sub(
            rf"(?m)^(?P<indent>[ \t]*)from[ \t]+{re.escape(original_stem)}[ \t]+import",
            rf"\g<indent>from {MODULE_NAME} import",
            test_code,
        )
        test_code = re.sub(
            rf"(?m)^(?P<indent>[ \t]*)import[ \t]+{re.escape(original_stem)}\b",
            rf"\g<indent>import {MODULE_NAME}",
            test_code,
        )
    return normalize_python_tests(test_code, source_code)


def _make_workspace(
    root: Path,
    label: str,
    *,
    source_code: str,
    test_code: str,
    project_files: Mapping[str, str] | None = None,
) -> Path:
    workspace = root / label
    workspace.mkdir(parents=True, exist_ok=False)
    (workspace / f"{MODULE_NAME}.py").write_text(source_code, encoding="utf-8")
    (workspace / TEST_FILE_NAME).write_text(test_code, encoding="utf-8")
    (workspace / REPORTER_FILE_NAME).write_text(_PYTEST_REPORTER, encoding="utf-8")
    (workspace / "__init__.py").write_text("", encoding="utf-8")
    write_project_files(
        workspace,
        project_files,
        reserved_paths=_WORKSPACE_RESERVED_PATHS,
    )
    if os.name == "posix":
        workspace.chmod(0o777)
        for file_path in workspace.iterdir():
            file_path.chmod(0o666)
    return workspace


def _base_env(workspace: Path) -> dict[str, str]:
    return {
        **os.environ,
        "PYTHONPATH": str(workspace),
        "HOME": str(workspace),
        "PYTHONDONTWRITEBYTECODE": "1",
        "PYTHONHASHSEED": "0",
        "PYTEST_DISABLE_PLUGIN_AUTOLOAD": "1",
        "TZ": "UTC",
        "COLUMNS": "120",
        "TERM": "dumb",
        "UTCODER_PYTEST_REPORT": str(workspace / "pytest_report.json"),
    }


def _run_process(
    command: list[str],
    *,
    workspace: Path,
    timeout: float,
    memory_mb: int,
    max_processes: int,
    max_output_bytes: int = 4 * 1024 * 1024,
) -> _ProcessOutcome:
    started = time.monotonic()
    try:
        process = run_limited_process(
            command,
            cwd=workspace,
            env=_base_env(workspace),
            timeout=timeout,
            memory_mb=memory_mb,
            cpu_seconds=max(5, int(timeout)),
            file_size_mb=32,
            max_files=256,
            max_processes=max_processes,
            max_output_bytes=max_output_bytes,
        )
        return _ProcessOutcome(
            returncode=process.returncode,
            stdout=process.stdout,
            stderr=process.stderr,
            duration_seconds=time.monotonic() - started,
        )
    except subprocess.TimeoutExpired as exc:
        return _ProcessOutcome(
            returncode=None,
            stdout=str(exc.stdout or ""),
            stderr=str(exc.stderr or ""),
            duration_seconds=time.monotonic() - started,
            timed_out=True,
        )
    except OSError as exc:
        return _ProcessOutcome(
            returncode=None,
            stdout="",
            stderr="",
            duration_seconds=time.monotonic() - started,
            start_error=str(exc),
        )


def _pytest_command(*, collect_only: bool, coverage: bool) -> list[str]:
    command = [
        sys.executable,
        "-m",
        "pytest",
        TEST_FILE_NAME,
        "-q",
        "--disable-warnings",
        "--tb=short",
        "-p",
        "no:cacheprovider",
        "-p",
        "utcoder_eval_plugin",
    ]
    if collect_only:
        command.append("--collect-only")
    if coverage:
        command.extend(
            [
                "-p",
                "pytest_cov.plugin",
                f"--cov={MODULE_NAME}",
                "--cov-branch",
                "--cov-report=json:coverage.json",
            ]
        )
    return command


def _integer(mapping: dict[str, Any], *names: str) -> int:
    for name in names:
        value = mapping.get(name)
        if isinstance(value, bool):
            continue
        if isinstance(value, (int, float)):
            return max(0, int(value))
    return 0


def _parse_pytest_report(
    path: Path,
    outcome: _ProcessOutcome,
) -> TestRunSummary:
    summary = TestRunSummary(
        exit_code=outcome.returncode,
        duration_seconds=outcome.duration_seconds,
        timed_out=outcome.timed_out,
    )
    if outcome.timed_out or outcome.start_error or not path.is_file():
        return summary
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
        counts = payload.get("summary")
        if not isinstance(counts, dict):
            return summary
        summary.collected = _integer(counts, "collected", "total")
        summary.passed = _integer(counts, "passed")
        summary.failed = _integer(counts, "failed")
        summary.errors = _integer(counts, "error", "errors")
        summary.skipped = _integer(counts, "skipped")
        summary.xfailed = _integer(counts, "xfailed")
        summary.xpassed = _integer(counts, "xpassed")
        summary.report_valid = True
        return summary
    except (OSError, TypeError, ValueError, json.JSONDecodeError):
        return summary


def _run_pytest(
    workspace: Path,
    *,
    collect_only: bool,
    coverage: bool,
    timeout: float,
    config: EvaluationConfig,
) -> tuple[TestRunSummary, _ProcessOutcome]:
    for artifact in ("pytest_report.json", "coverage.json", ".coverage"):
        (workspace / artifact).unlink(missing_ok=True)
    outcome = _run_process(
        _pytest_command(collect_only=collect_only, coverage=coverage),
        workspace=workspace,
        timeout=timeout,
        memory_mb=config.memory_mb,
        max_processes=min(config.max_processes, 24),
    )
    return _parse_pytest_report(workspace / "pytest_report.json", outcome), outcome


def _ratio_percent(covered: int, total: int) -> float:
    return 100.0 if total <= 0 else 100.0 * covered / total


def _parse_coverage(path: Path) -> CoverageSummary:
    result = CoverageSummary()
    if not path.is_file():
        return result
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
        files = payload.get("files", {})
        module_data = next(
            (
                value
                for key, value in files.items()
                if Path(key).name == f"{MODULE_NAME}.py" and isinstance(value, dict)
            ),
            None,
        )
        if module_data is None:
            return result
        totals = module_data.get("summary", {})
        if not isinstance(totals, dict):
            return result
        result.num_statements = _integer(totals, "num_statements")
        result.covered_lines = _integer(totals, "covered_lines")
        result.num_branches = _integer(totals, "num_branches")
        result.covered_branches = _integer(totals, "covered_branches")

        line_percent = totals.get("percent_statements_covered")
        if not isinstance(line_percent, (int, float)):
            line_percent = _ratio_percent(result.covered_lines, result.num_statements)
        branch_percent = totals.get("percent_branches_covered")
        if result.num_branches == 0:
            branch_percent = line_percent
        elif not isinstance(branch_percent, (int, float)):
            branch_percent = _ratio_percent(
                result.covered_branches, result.num_branches
            )
        result.line_percent = round(float(line_percent), 4)
        result.branch_percent = round(float(branch_percent), 4)
        result.valid = True
        return result
    except (OSError, TypeError, ValueError, json.JSONDecodeError):
        return result


def parse_mutmut_progress(output: str, *, duration_seconds: float = 0.0) -> MutationSummary:
    """Parse and validate mutmut 3's final progress counter.

    Mutmut currently has no documented JSON result format. The adapter is
    deliberately strict: a changed/partial format yields MUTATION_INCOMPLETE
    instead of silently manufacturing a score.
    """

    cleaned = _ANSI_ESCAPE.sub("", str(output or ""))
    fragments = re.split(r"[\r\n]+", cleaned)
    best: tuple[int, int, dict[str, int]] | None = None
    for fragment in fragments:
        progress = re.search(r"(?P<done>\d+)\s*/\s*(?P<total>\d+)", fragment)
        if not progress:
            continue
        counts: dict[str, int] = {}
        for name, emoji in _MUTMUT_EMOJIS.items():
            matches = re.findall(re.escape(emoji) + r"\ufe0f?\s*(\d+)", fragment)
            if matches:
                counts[name] = int(matches[-1])
        if len(counts) < 4:
            continue
        candidate = (int(progress.group("done")), int(progress.group("total")), counts)
        if best is None or candidate[0] >= best[0]:
            best = candidate

    if best is None:
        return MutationSummary(
            duration_seconds=duration_seconds,
            reason="Unsupported or missing mutmut progress summary.",
        )

    completed, total, counts = best
    classified = sum(counts.values())
    complete = completed == total and classified == total
    reason = "" if complete else (
        f"Partial/inconsistent mutmut summary: completed={completed}, "
        f"classified={classified}, total={total}."
    )
    return MutationSummary(
        killed=counts.get("killed", 0),
        timed_out=counts.get("timed_out", 0),
        segfault=counts.get("segfault", 0),
        survived=counts.get("survived", 0),
        no_tests=counts.get("no_tests", 0),
        suspicious=counts.get("suspicious", 0),
        skipped=counts.get("skipped", 0),
        interrupted=counts.get("interrupted", 0),
        total=total,
        completed=completed,
        duration_seconds=duration_seconds,
        complete=complete,
        reason=reason,
    )


def _mutation_config() -> str:
    return """[tool.mutmut]
source_paths = ["src/"]
only_mutate = ["src/module_under_test.py"]
pytest_add_cli_args_test_selection = ["tests/test_module_under_test.py"]
pytest_add_cli_args = ["-q", "-p", "no:cacheprovider"]
use_git_change_detection = false
mutate_only_covered_lines = false
timeout_constant = 1.0
timeout_multiplier = 5.0
"""


def _run_mutation(
    root: Path,
    *,
    source_code: str,
    test_code: str,
    project_files: Mapping[str, str] | None = None,
    config: EvaluationConfig,
) -> tuple[MutationSummary, _ProcessOutcome]:
    workspace = root / "mutation"
    source_dir = workspace / "src"
    tests_dir = workspace / "tests"
    source_dir.mkdir(parents=True)
    tests_dir.mkdir(parents=True)
    source_path = source_dir / f"{MODULE_NAME}.py"
    source_path.write_text(source_code, encoding="utf-8")
    write_project_files(
        source_dir,
        project_files,
        reserved_paths={f"{MODULE_NAME}.py"},
    )
    (tests_dir / TEST_FILE_NAME).write_text(test_code, encoding="utf-8")
    (tests_dir / "conftest.py").write_text(
        "import sys\n"
        "from pathlib import Path\n"
        "sys.path.insert(0, str(Path(__file__).resolve().parents[1] / 'src'))\n",
        encoding="utf-8",
    )
    (workspace / "pyproject.toml").write_text(_mutation_config(), encoding="utf-8")
    if os.name == "posix":
        workspace.chmod(0o777)
        source_dir.chmod(0o777)
        tests_dir.chmod(0o777)
        for file_path in (
            source_path,
            tests_dir / TEST_FILE_NAME,
            tests_dir / "conftest.py",
            workspace / "pyproject.toml",
        ):
            file_path.chmod(0o666)

    executable = shutil.which("mutmut") or "mutmut"
    outcome = _run_process(
        [executable, "run", "--max-children", str(config.mutation_max_children)],
        workspace=workspace,
        timeout=config.mutation_timeout_seconds,
        memory_mb=config.mutation_memory_mb,
        max_processes=config.max_processes,
        max_output_bytes=8 * 1024 * 1024,
    )
    if outcome.timed_out:
        return MutationSummary(
            duration_seconds=outcome.duration_seconds,
            reason="Global mutation-stage timeout expired.",
        ), outcome
    if outcome.start_error:
        return MutationSummary(
            duration_seconds=outcome.duration_seconds,
            reason=f"Could not start mutmut: {outcome.start_error}",
        ), outcome
    return parse_mutmut_progress(
        outcome.output,
        duration_seconds=outcome.duration_seconds,
    ), outcome


def _write_artifacts(
    artifact_dir: str | Path | None,
    *,
    source_code: str,
    test_code: str,
    project_files: Mapping[str, str] | None,
    result: EvaluationResult,
) -> None:
    if artifact_dir is None:
        return
    target = Path(artifact_dir)
    target.mkdir(parents=True, exist_ok=True)
    (target / "source.py").write_text(source_code, encoding="utf-8")
    (target / "generated_test.py").write_text(test_code, encoding="utf-8")
    if project_files:
        write_project_files(target / "project_files", project_files)
    (target / "eval_result.json").write_text(
        json.dumps(result.to_dict(), ensure_ascii=False, indent=2),
        encoding="utf-8",
    )


def evaluate_python_test(
    *,
    file_name: str,
    source_code: str,
    test_code: str,
    project_files: Mapping[str, str] | None = None,
    artifact_dir: str | Path | None = None,
    config: EvaluationConfig | None = None,
) -> EvaluationResult:
    """Run all quality gates and return a versioned evaluation result."""

    started = time.monotonic()
    config = config or EvaluationConfig.from_environment()
    project_files = normalize_project_files(
        project_files,
        reserved_paths=_WORKSPACE_RESERVED_PATHS,
    )
    normalized_test = _prepare_test_code(file_name, source_code, test_code)
    result = EvaluationResult.empty(
        EvaluationStatus.NO_GENERATED_TEST,
        source_code=source_code,
        test_code=normalized_test,
    )
    if not normalized_test.strip():
        result.finalize_invalid_score()
        result.duration_seconds = time.monotonic() - started
        _write_artifacts(
            artifact_dir,
            source_code=source_code,
            test_code=normalized_test,
            project_files=project_files,
            result=result,
        )
        return result

    with tempfile.TemporaryDirectory(prefix="utcoder-eval-") as temp_dir:
        root = Path(temp_dir)
        if os.name == "posix":
            root.chmod(0o777)

        compile_workspace = _make_workspace(
            root,
            "compile",
            source_code=source_code,
            test_code=normalized_test,
            project_files=project_files,
        )
        support_python_files = sorted(
            path for path in project_files if path.endswith(".py")
        )
        compile_outcome = _run_process(
            [
                sys.executable,
                "-m",
                "py_compile",
                f"{MODULE_NAME}.py",
                TEST_FILE_NAME,
                *support_python_files,
            ],
            workspace=compile_workspace,
            timeout=config.compile_timeout_seconds,
            memory_mb=min(config.memory_mb, 512),
            max_processes=8,
        )
        if compile_outcome.timed_out:
            result.status = EvaluationStatus.COMPILE_TIMEOUT.value
            result.diagnostics.append("Python compile stage timed out.")
        elif compile_outcome.returncode != 0:
            output = compile_outcome.output
            result.status = (
                EvaluationStatus.TEST_COMPILE_FAILED.value
                if TEST_FILE_NAME in output
                else EvaluationStatus.SOURCE_COMPILE_FAILED.value
            )
            result.diagnostics.append(_short_log(output or compile_outcome.start_error))
        if result.status != EvaluationStatus.NO_GENERATED_TEST.value:
            result.finalize_invalid_score()
            result.duration_seconds = time.monotonic() - started
            _write_artifacts(
                artifact_dir,
                source_code=source_code,
                test_code=normalized_test,
                project_files=project_files,
                result=result,
            )
            return result

        collection_workspace = _make_workspace(
            root,
            "collection",
            source_code=source_code,
            test_code=normalized_test,
            project_files=project_files,
        )
        collection, collection_outcome = _run_pytest(
            collection_workspace,
            collect_only=True,
            coverage=False,
            timeout=config.collection_timeout_seconds,
            config=config,
        )
        result.collection = collection
        if not collection.report_valid or collection.exit_code not in (0, 5):
            result.status = EvaluationStatus.COLLECTION_FAILED.value
            result.diagnostics.append(
                _short_log(collection_outcome.output or collection_outcome.start_error)
            )
        elif collection.collected <= 0 or collection.exit_code == 5:
            result.status = EvaluationStatus.NO_TESTS.value
        if result.status != EvaluationStatus.NO_GENERATED_TEST.value:
            result.finalize_invalid_score()
            result.duration_seconds = time.monotonic() - started
            _write_artifacts(
                artifact_dir,
                source_code=source_code,
                test_code=normalized_test,
                project_files=project_files,
                result=result,
            )
            return result

        run_logs: list[_ProcessOutcome] = []
        for run_index in range(1, 4):
            run_workspace = _make_workspace(
                root,
                f"stability_{run_index}",
                source_code=source_code,
                test_code=normalized_test,
                project_files=project_files,
            )
            run, run_outcome = _run_pytest(
                run_workspace,
                collect_only=False,
                coverage=False,
                timeout=config.baseline_timeout_seconds,
                config=config,
            )
            result.stability_runs.append(run)
            run_logs.append(run_outcome)

        result.stability_passed_runs = sum(
            int(run.successful) for run in result.stability_runs
        )
        if all(run.all_skipped for run in result.stability_runs):
            result.status = EvaluationStatus.ALL_SKIPPED.value
        elif result.stability_passed_runs <= 1:
            result.status = EvaluationStatus.UNSTABLE.value
        if result.status != EvaluationStatus.NO_GENERATED_TEST.value:
            result.finalize_invalid_score()
            result.diagnostics.extend(
                _short_log(log.output or log.start_error)
                for run, log in zip(result.stability_runs, run_logs)
                if not run.successful and (log.output or log.start_error)
            )
            result.duration_seconds = time.monotonic() - started
            _write_artifacts(
                artifact_dir,
                source_code=source_code,
                test_code=normalized_test,
                project_files=project_files,
                result=result,
            )
            return result

        result.valid = True
        result.stable = result.stability_passed_runs == 3
        result.status = (
            EvaluationStatus.VALID_STABLE.value
            if result.stable
            else EvaluationStatus.VALID_FLAKY.value
        )

        # A flaky suite gets up to three fresh chances to produce coverage from
        # a passing run. Stable suites need only one additional execution.
        coverage_attempts = 1 if result.stable else 3
        coverage_outcome: _ProcessOutcome | None = None
        for coverage_index in range(1, coverage_attempts + 1):
            coverage_workspace = _make_workspace(
                root,
                f"coverage_{coverage_index}",
                source_code=source_code,
                test_code=normalized_test,
                project_files=project_files,
            )
            coverage_run, coverage_outcome = _run_pytest(
                coverage_workspace,
                collect_only=False,
                coverage=True,
                timeout=config.coverage_timeout_seconds,
                config=config,
            )
            coverage = _parse_coverage(coverage_workspace / "coverage.json")
            if coverage_run.successful and coverage.valid:
                result.coverage_run = coverage_run
                result.coverage = coverage
                break
        if not result.coverage.valid:
            result.status = EvaluationStatus.COVERAGE_FAILED.value
            result.finalize_invalid_score()
            if coverage_outcome is not None:
                result.diagnostics.append(
                    _short_log(coverage_outcome.output or coverage_outcome.start_error)
                )
            result.duration_seconds = time.monotonic() - started
            _write_artifacts(
                artifact_dir,
                source_code=source_code,
                test_code=normalized_test,
                project_files=project_files,
                result=result,
            )
            return result

        mutation, mutation_outcome = _run_mutation(
            root,
            source_code=source_code,
            test_code=normalized_test,
            project_files=project_files,
            config=config,
        )
        result.mutation = mutation
        if not mutation.complete:
            result.status = EvaluationStatus.MUTATION_INCOMPLETE.value
            result.score_complete = False
            result.diagnostics.append(
                mutation.reason
                or _short_log(mutation_outcome.output or mutation_outcome.start_error)
            )
        else:
            result.finalize_score()

    result.duration_seconds = time.monotonic() - started
    _write_artifacts(
        artifact_dir,
        source_code=source_code,
        test_code=normalized_test,
        project_files=project_files,
        result=result,
    )
    return result


def run_evaluator_preflight() -> EvaluationResult:
    source = """def classify(value):
    if value > 0:
        return "positive"
    return "non-positive"
"""
    tests = """from module_under_test import classify

def test_positive():
    assert classify(1) == "positive"

def test_non_positive():
    assert classify(0) == "non-positive"
"""
    result = evaluate_python_test(
        file_name="preflight_quality.py",
        source_code=source,
        test_code=tests,
    )
    if not result.valid or not result.score_complete or not result.mutation.complete:
        detail = "; ".join(result.diagnostics[-2:]) or result.status
        raise RuntimeError(f"Benchmark evaluator preflight failed: {detail}")
    return result


def _parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Evaluate one generated Python test suite.")
    parser.add_argument("--source-file")
    parser.add_argument("--test-file")
    parser.add_argument("--output", default="eval_result.json")
    parser.add_argument("--artifact-dir")
    parser.add_argument("--self-test", action="store_true")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = _parse_args(argv)
    if args.self_test:
        result = run_evaluator_preflight()
    else:
        if not args.source_file or not args.test_file:
            raise SystemExit("--source-file and --test-file are required")
        source_path = Path(args.source_file)
        test_path = Path(args.test_file)
        result = evaluate_python_test(
            file_name=source_path.name,
            source_code=source_path.read_text(encoding="utf-8"),
            test_code=test_path.read_text(encoding="utf-8"),
            artifact_dir=args.artifact_dir,
        )
    Path(args.output).write_text(
        json.dumps(result.to_dict(), ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(
        f"Evaluator: status={result.status}, valid={result.valid}, "
        f"score={result.final_score}"
    )
    return 0 if result.score_complete else 1


if __name__ == "__main__":
    raise SystemExit(main())
